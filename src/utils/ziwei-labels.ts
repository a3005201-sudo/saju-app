export function withKoreanLabel(hanja: string): string {
  const ko = ZIWEI_KO_LABELS[hanja]
  return ko ? `${hanja}(${ko})` : hanja
}

export function ageWithKorean(ageText: string): string {
  return ageText.replaceAll('歲', '세')
}
